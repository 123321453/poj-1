public class p1517 {
    public static int factorial(int n){
        if(n == 0 || n == 1)
            return 1;
        else
            return n*factorial(n-1);
    }
    public static void main(String[] args) {
        double[] tab = new double[10];
        for(int i = 0; i <= 9;i++){
            if(i == 0)
                tab[i] = 1;
            else if(i == 1)
                tab[i] = 2;
            else
                tab[i] = (double)tab[i-1] + (double)1/factorial(i);
        }
        System.out.println("n e");
        System.out.println("- -----------");
        for(int i = 0; i <= 1 ; i++){
            System.out.printf("%d %.0f\n",i,tab[i]);
        }
        System.out.printf("2 %.1f\n",tab[2]);
        for(int i = 3; i <= 9 ; i++){
            System.out.printf("%d %.9f\n",i,tab[i]);
        }
    }
}
